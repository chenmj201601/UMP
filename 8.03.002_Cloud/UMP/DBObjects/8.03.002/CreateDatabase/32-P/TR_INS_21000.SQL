CREATE TRIGGER TR_INS_21000
  BEFORE INSERT ON T_21_000
  FOR EACH ROW
DECLARE
     LIntC00 NUMBER;
BEGIN    

    SELECT C004+1 INTO LIntC00  FROM T_00_001
       WHERE C000 = '00000' AND C001 = 21
             AND C002 = 201 AND C003 = 14010100;

    UPDATE T_00_001
       SET c004 = LIntC00
     WHERE c000 ='00000'
       AND c001 = 21
       AND c002 = 201
       AND c003 = 14010100;

    :NEW.C000:=LIntC00;
    :NEW.C101:=0;
    :NEW.C102:='';

END;